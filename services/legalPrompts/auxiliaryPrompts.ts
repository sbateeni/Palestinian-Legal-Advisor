export const getResearchPrompt = (query: string, region: LegalRegion) => {
    const regionName = region === 'gaza' ? 'قطاع غزة' : 'الضفة الغربية';
    return `${getBaseInstruction(region, 'chat')}
${LEGISLATIVE_VERSION_PROTOCOL}
قم بإجراء بحث قانوني معمق حول الموضوع التالي: "${query}" في بيئة ${regionName}.

**استراتيجية البحث في صفحة البحث (إلزامية قبل الإقرار بعدم العثور):**
1. نفّذ تسلسلاً من الاستعلامات (على الأقل 3 صيغ مختلفة)، مثل:
   - "${query}" + ${regionName}
   - "${query}" + تعديل OR تعديلات OR ساري OR إلغاء
   - "${query}" + site:ogb.gov.ps OR site:birzeit.edu OR site:dftp.gov.ps
2. إذا كان الطلب يخص قانوناً باسم محدد: جرّب رقم القانون وحده، والاسم المختصر، والبحث داخل "المقتفي" و"الجريدة الرسمية".
3. **لا تكتب "لم يتم العثور"** إلا بعد تنفيذ الخطوات أعلاه وتسمية الاستعلامات التي جرّبتها.

**قواعد صارمة إضافية (منع الهلوسة):**
1. لا تذكر أي مادة/نص إلا إذا كان لديك مصدر تحقق عبر البحث.
2. عند ذكر أي مادة قانونية: اذكر فوراً (اسم القانون + رقم المادة) ثم أرفق رابطاً مباشراً للمصدر الذي تم العثور عليه.
3. ميّز بين النص الساري وأي نسخة قديمة إذا ظهرت في المصادر.

**النتيجة المطلوبة:**
صلب الموضوع أولاً؛ إن دعت الحاجة جدول "بطاقة الحالة" مختصر ثم التحليل مع روابط مباشرة، دون حشو جانبي. `;
};

